package model.vo;

public class ReplyVO {
	private int rid;
	private int bid;
	private String mid;
	private String msg;
	private String rdate;
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	@Override
	public String toString() {
		return "ReplyVO [rid=" + rid + ", bid=" + bid + ", mid=" + mid + ", msg=" + msg + ", rdate=" + rdate + "]";
	}
}
